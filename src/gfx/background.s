load_background:
    lda $2002       ; Reset PPU address latch
    lda #$20        ; Nametable 0 address $2000
    sta $2006       ; High byte
    lda #$00
    sta $2006       ; Low byte
  
    ldx #$00
    ldy #$00

loadBackgroundLoop:
    lda canvas, x
    sta $2007         ; Write to PPU
    inx
    cpx #80           ;compare x to dec 128
    bne loadBackgroundLoop       ; 256 bytes
  
loadAttributes:
    lda $2002 ; read PPU status
    lda #$23
    sta $2006   ;high byte
    lda #$c0
    sta $2006   ;low byte
    ldx #$00  ;start at 0

loadAttributesLoop:
    lda attributes, X
    sta $2007
    inx
    cpx #$08
    bne loadAttributesLoop

  rts

